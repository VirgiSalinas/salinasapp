// FUNCIONES DE ACCESO A DATOS


/**
 * Obtiene todos los médicos
 * @returns {Promise<Array>} Array con todos los médicos
 */
export const getMedicos = () => {
  return new Promise((resolve) => {
    setTimeout(() => {
      resolve(medicos);
    }, 800);
  });
};

/**
 * Obtiene médicos filtrados por categoría
 @param {string} categoryId 
 * @returns {Promise<Array>} Array con médicos filtrados
 */
export const getMedicosByCategory = (categoryId) => {
  return new Promise((resolve) => {
    setTimeout(() => {
      resolve(
        medicos.filter((medico) => medico.category === categoryId)
      );
    }, 800);
  });
};

/**
 * Obtiene un médico específico por ID
 * @param {string|number} id - ID del médico
 * @returns {Promise<Object|undefined>} Objeto médico o undefined si no existe
 */
export const getMedicoById = (id) => {
  return new Promise((resolve) => {
    setTimeout(() => {
      resolve(
        medicos.find((medico) => medico.id === Number(id))
      );
    }, 800);
  });
};
